// Створюємо змінну для зберігання суми
let sum = 0;
// Задаємо цикл від 1 до 100 включно
for (let i = 1; i <= 100; i++) {
    // Задаємо умову що число не кратне 3
    if (i % 3 !== 0) {
        // Додаємо це число до суми
        sum += i;
    }
}
// Виводимо результат на екран
console.log('Сума чисел від 1 до 100, які не кратні 3:', sum);
